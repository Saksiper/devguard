'use strict';

// ONE-OFF fixture generator (NOT run in CI). Loads the real @xenova MiniLM model and
// produces 384-dim embeddings for realistic same-feature-different-file description
// sets plus distinct features for separation. Output is committed as a JSON fixture;
// tests load the JSON only and never touch the transformer.
//
//   DEVGUARD_OFFLINE=1 node tools/gen-feature-fixture.js
//
// The `feature` key groups samples that should collapse to one node_id; `continent`
// is the expected model-free classification.

const fs = require('fs');
const path = require('path');
const { loadModel, encode, EMBEDDING_DIM } = require('../src/engine/embedding');

// [file, description, continent, feature]
const DATASET = [
  // security / user-auth — cross-file cohesion + wording-robustness set (6 variants)
  ['auth.js', 'add JWT login flow', 'security', 'user-auth'],
  ['login.js', 'validate user credentials on sign in', 'security', 'user-auth'],
  ['token.js', 'refresh the access token', 'security', 'user-auth'],
  ['session-auth.js', 'authenticate the user and issue a session token', 'security', 'user-auth'],
  ['signin.js', 'log the user in and verify their password', 'security', 'user-auth'],
  ['oauth-login.js', 'complete the OAuth login and store the auth token', 'security', 'user-auth'],
  // security / encryption — separation within the same continent
  ['crypto.js', 'encrypt file contents using AES-256', 'security', 'encryption'],
  ['cipher.js', 'decrypt the AES encrypted payload', 'security', 'encryption'],

  // ui_ux / product-filter — cross-file differently named
  ['filter-panel.jsx', 'render the product filter sidebar', 'ui_ux', 'product-filter'],
  ['filters.js', 'apply the selected filter chips to the results', 'ui_ux', 'product-filter'],
  ['filter-bar.tsx', 'filter the catalog by category and price', 'ui_ux', 'product-filter'],
  // ui_ux / modal-dialog — separation within ui_ux
  ['modal.jsx', 'open a confirmation modal dialog', 'ui_ux', 'modal-dialog'],
  ['dialog.tsx', 'close the modal dialog on the escape key', 'ui_ux', 'modal-dialog'],

  // data / db-migration
  ['001-init.sql', 'create the users table schema', 'data', 'db-migration'],
  ['migrate.js', 'run the database migration for orders', 'data', 'db-migration'],
  ['schema.js', 'define the product model schema and columns', 'data', 'db-migration'],
  // data / analytics — separation within data
  ['analytics.js', 'query the events table for daily active users', 'data', 'analytics'],

  // math / linear-algebra
  ['matrix.js', 'multiply two matrices together', 'math', 'linear-algebra'],
  ['vectors.js', 'compute the dot product of two vectors', 'math', 'linear-algebra'],
  ['geometry.js', 'calculate the area of a polygon', 'math', 'linear-algebra'],
  ['transform.js', 'apply a rotation matrix to the vector', 'math', 'linear-algebra'],

  // infra / deployment
  ['Dockerfile', 'build the production container image', 'infra', 'deployment'],
  ['deploy.yaml', 'kubernetes deployment manifest for the api', 'infra', 'deployment'],
  ['ci.yml', 'github actions pipeline to run the test suite', 'infra', 'deployment'],

  // docs / documentation
  ['README.md', 'document the installation steps', 'docs', 'documentation'],
  ['setup-guide.md', 'explain the configuration options', 'docs', 'documentation'],

  // logic / pricing
  ['pricing.js', 'compute the discounted order total', 'logic', 'pricing'],
  ['checkout.js', 'recalculate the cart total with tax and shipping', 'logic', 'pricing'],
  // logic / scheduler — separation within logic
  ['scheduler.js', 'schedule a recurring background job', 'logic', 'scheduler'],
  ['queue.js', 'process the next task from the job queue', 'logic', 'scheduler'],
];

async function main() {
  const model = await loadModel();
  if (!model) {
    console.error('Model failed to load — cannot generate fixture. Is the MiniLM model cached in DEVGUARD_MODEL_DIR?');
    process.exit(1);
  }

  const samples = [];
  for (const [file, description, continent, feature] of DATASET) {
    const buf = await encode(description);
    if (!buf) {
      console.error('encode returned null for', description);
      process.exit(1);
    }
    const f32 = new Float32Array(buf.buffer, buf.byteOffset, buf.byteLength / 4);
    // Round to 6 sig-figs to keep the committed JSON compact but well within
    // float32 precision (differences show up ~1e-7, far below any threshold gap).
    const embedding = Array.from(f32, (x) => Number(x.toFixed(6)));
    samples.push({ file, description, continent, feature, embedding });
  }

  const out = {
    model: 'Xenova/all-MiniLM-L6-v2',
    dim: EMBEDDING_DIM,
    note: 'Generated by tools/gen-feature-fixture.js. Do not edit by hand.',
    samples,
  };

  const dest = path.join(__dirname, '..', 'tests', 'fixtures', 'feature-embeddings.json');
  fs.mkdirSync(path.dirname(dest), { recursive: true });
  fs.writeFileSync(dest, JSON.stringify(out, null, 2));
  console.log('Wrote', samples.length, 'samples ->', dest);
}

main();
